def add_styled_slide(prs: Presentation, title: str, body: str) -> None:
    slide = prs.slides.add_slide(prs.slide_layouts[6])

    bg = slide.background.fill
    bg.solid()
    bg.fore_color.rgb = COLORS["bg"]

    accent_bar = slide.shapes.add_shape(
        MSO_AUTO_SHAPE_TYPE.RECTANGLE, Inches(0), Inches(0), Inches(13.333), Inches(0.18)
    )
    accent_bar.fill.solid()
    accent_bar.fill.fore_color.rgb = COLORS["accent"]
    accent_bar.line.fill.background()

    card = slide.shapes.add_shape(
        MSO_AUTO_SHAPE_TYPE.ROUNDED_RECTANGLE, Inches(0.6), Inches(1.1), Inches(12.1), Inches(5.9)
    )
    card.fill.solid()
    card.fill.fore_color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
    card.line.color.rgb = COLORS["border"]
    card.line.width = Pt(1)


    tbox = slide.shapes.add_textbox(Inches(1.0), Inches(1.45), Inches(11.4), Inches(1.0))
    tf = tbox.text_frame
    tf.clear()
    p = tf.paragraphs[0]
    run = p.add_run()
    run.text = title
    run.font.name = "Segoe UI Semibold"
    run.font.size = Pt(34)
    run.font.bold = True
    run.font.color.rgb = COLORS["title"]
    p.alignment = PP_ALIGN.LEFT


    line = slide.shapes.add_shape(
        MSO_AUTO_SHAPE_TYPE.RECTANGLE, Inches(1.0), Inches(2.35), Inches(2.0), Inches(0.06)
    )
    line.fill.solid()
    line.fill.fore_color.rgb = COLORS["accent"]
    line.line.fill.background()


    bbox = slide.shapes.add_textbox(Inches(1.0), Inches(2.65), Inches(11.0), Inches(3.9))
    btf = bbox.text_frame
    btf.clear()
    btf.word_wrap = True
    paragraph = btf.paragraphs[0]
    paragraph.text = body
    paragraph.font.name = "Segoe UI"
    paragraph.font.size = Pt(21)
    paragraph.font.color.rgb = COLORS["text"]
    paragraph.alignment = PP_ALIGN.LEFT
    paragraph.space_after = Pt(6)
    paragraph.line_spacing = 1.35


    footer = slide.shapes.add_textbox(Inches(10.3), Inches(7.05), Inches(2.2), Inches(0.35))
    ftf = footer.text_frame
    ftf.clear()
    fp = ftf.paragraphs[0]
    fr = fp.add_run()
    fr.text = "AERO Project"
    fr.font.name = "Segoe UI"
    fr.font.size = Pt(11)
    fr.font.color.rgb = COLORS["muted"]
    fp.alignment = PP_ALIGN.RIGHT


def main() -> None:
    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    for title, body in SLIDES:
        add_styled_slide(prs, title, body)

    prs.save(OUTPUT)
    print(OUTPUT)


if __name__ == "__main__":
    main()
